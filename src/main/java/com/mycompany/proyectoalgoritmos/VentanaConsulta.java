    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String contenidoBuscar = buscarIdField.getText();
        
        if(contenidoBuscar.equalsIgnoreCase("")){
           acciones.actualizarTabla();
        }
        try {
        int clave = Integer.parseInt(contenidoBuscar);
        acciones.buscarProducto(clave);
    } catch (NumberFormatException e) {
        acciones.ordenarYFiltrar(contenidoBuscar);
    } 
    }//GEN-LAST:event_jButton5ActionPerformed

