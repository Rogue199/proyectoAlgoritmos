    private int guardarFilaSeleccionada() {
        filaSeleccionada = tablaProductos.getSelectedRow();
        if (filaSeleccionada != -1) {
            Object nombre = tablaProductos.getValueAt(filaSeleccionada, 0);
            Object valor = tablaProductos.getValueAt(filaSeleccionada, 1);

            System.out.println("Fila seleccionada guardada:");
            System.out.println("Nombre: " + nombre);
            System.out.println("Valor: " + valor);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione un producto.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return -1;
        }
        return 0;
    }
    
